// In this kata you will create a function that takes in a list and returns a list with the reverse order.
// Examples (Input -> Output)
// * [1, 2, 3, 4]  -> [4, 3, 2, 1]
// * [9, 2, 0, 7]  -> [7, 0, 2, 9]


const listReverse = (arr) => {
    return arr.reverse();
}

console.log(listReverse([1, 2, 3, 4]));
console.log(listReverse([9, 2, 0, 7]));
